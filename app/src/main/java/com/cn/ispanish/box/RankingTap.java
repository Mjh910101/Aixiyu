public class RankingTap {

    String name;
    String id;

    public RankingTap(JSONObject json) {
        name = JsonHandle.getString(json, "name");
        id = JsonHandle.getString(json, "id");
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
