public class PaperContentView extends LinearLayout {

    private Context context;
    private View view;
    private LayoutInflater inflater;

    private TextView testView;

    public PaperContentView(Context context) {
        super(context);

        this.context = context;
        inflater = (LayoutInflater) context
                .getSystemService(Context.LAYOUT_INFLATER_SERVICE);

        view = inflater.inflate(R.layout.layout_paper, null);
        testView = (TextView) view.findViewById(R.id.paper_testView);

        view.setLayoutParams(new ViewPager.LayoutParams());
        addView(view);
    }

    public void setTestView(String str) {
        testView.setText(str);
    }


}
